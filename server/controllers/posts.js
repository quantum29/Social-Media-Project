/* UPDATE */
export const likePost = async (req, res) => {
  try {
    const { id } = req.params; // id comes from the query string, grabs the relevant post
    const { userId } = req.body; // user id comes from the body ,grabs the user id  { we are expecting to send like a json object from the frontend}
    const post = await Post.findById(id); // we found the post from the id in the link from the db
    const isLiked = post.likes.get(userId); // we gonna check in the likes that if the user id exits . That means post has been liked by that particular user

    if (isLiked) {
      post.likes.delete(userId);
    } else {
      post.likes.set(userId, true);
    }
//now update that post now 
    const updatedPost = await Post.findByIdAndUpdate(
      id,
      { likes: post.likes },
      { new: true }
    );

    res.status(200).json(updatedPost);
  } catch (err) {
    res.status(404).json({ message: err.message });
  }
};